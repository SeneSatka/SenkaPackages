export function ascii(
  texts: string /*  size: "1" | "2" | "3", type: "normal" */
) {
  for (const text of texts.split("\n")) {
    for (let i = 0; i < 7; i++) {
      let row = "";

      for (const letter of text) {
        if (!Object.keys(letters).includes(letter.toLowerCase()))
          row += eval(`letters['?'][i]`) + " ";
        else if (letter.toLowerCase() == '"')
          row += eval(`letters['${letter.toLowerCase()}'][i]`) + " ";
        else row += eval(`letters["${letter.toLowerCase()}"][i]`) + " ";
      }

      console.log(row);
    }
    console.log("\n");
  }
}
